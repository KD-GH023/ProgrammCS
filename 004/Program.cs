if (N==3) Console.WriteLine("Wednesday");
if (N==4) Console.WriteLine("Thursday");
if (N==5) Console.WriteLine("Friday");
if (N==6) Console.WriteLine("Saturday");
if (N==7) Console.WriteLine("Sunday");